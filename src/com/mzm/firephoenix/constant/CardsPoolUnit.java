package com.mzm.firephoenix.constant;

public class CardsPoolUnit {
	private byte[] first;
	private byte[] keep;
	private int winType;
	private byte[] second;
	public byte[] getFirst() {
		return first;
	}
	public void setFirst(byte[] first) {
		this.first = first;
	}
	public byte[] getKeep() {
		return keep;
	}
	public void setKeep(byte[] keep) {
		this.keep = keep;
	}
	public int getWinType() {
		return winType;
	}
	public void setWinType(int winType) {
		this.winType = winType;
	}
	public byte[] getSecond() {
		return second;
	}
	public void setSecond(byte[] second) {
		this.second = second;
	}
}
